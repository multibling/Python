# What is input()
'''
A built-in Python function that stops
your program to get user input.

Using the input() alone reads the user's response
but immediately discards it.

To keep the value, assign it to a variable.
'''

name = input("Enter your name: ")       # Dynamic value
country = "South Africa"        # Hard-Coded

print("You are", name)

# Hard-Coded (static) value
'''
Fixed piece of data written directly into your code
that never changes at runtime.
'''

# Dynamic Values
'''
Data entered by the user, that can
vary each time the program runs.
'''
print(" ")
print(name, "comes from", country)

'''
Input() lets your program ask questions
and react to what the user types, making it feel alive.
'''
